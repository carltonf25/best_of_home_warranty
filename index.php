        <h1><strong>Home Warranty Basics:</strong></h1>
        <p>Home warranties are extremely popular among every type of homeowner.</p>
        <p>In essence, nearly every home warranty company out there is a type of service contract.  By paying an annual or monthly fee, the home warranty company will dispatch one of their pre-vetted technicians to perform repairs in the home.  By having a home warranty, homeowners can save upwards of $5000 in repairs.  Often a home warranty is also included with the sale of the home which can protect the buyer from unexpected and costly repairs.  Home warranties aren't always beneficial; if you are pretty handy yourself and can fix or troubleshoot minor issues then perhaps a home warranty isn't for you.</p>
        <p>As with any business, home warranty companies will have inclusions and exclusions.  A good rule of thumb for home warranty coverage is if it's inside the home, it is covered.  There are exceptions to this however; many home warranty companies do not cover pre-existing conditions, improperly sized units or insufficiently maintained units.  Some home warranty companies are more strict in their exclusions than others and some have higher caps on the amount paid per year.  With so many options to choose from, it is difficult to compare them efficiently.</p>
