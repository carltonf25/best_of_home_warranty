    <h1 itemprop="name" class="article-title">Getting the Most out of a Home Warranty</h1>
    <small>Posted by: Carlton on <em>June 7th, 2016</em></small><br><br>

    <p align="center"><img class="article-img" src="http://amppob.com/wp-content/uploads/2015/11/Home-in-hands.jpg" alt="home in your hands"></p>

    <h2>Understand what is <em>not</em> covered before making a purchase.</h2><br>
    <p class="article-content">Though a home warranty can be a great investment, it's important to know if it's the right investment for you before making a purchase, so let's go over a few categories of circumstances that most home warranty services would not cover.</p>

    <span class="blockquote"><h4><strong>Equipment failures that occurred before the contract was active</strong></h4>
    <p class="article-content">If a mechanical failure occurred before your home warranty was purchased, it's not likely to be covered.</p>

    <h4><strong>Damage or failure that was not caused by general wear over time</strong></h4>
    <p class="article-content">Think abnormal circumstances, like the local squirrel population deciding to take up residence in your air conditioner, or your teenager accidentally hitting your water heater when pulling into the garage. These would not be covered typically, so you'll want to be sure you're keeping your appliances safe from these kinds of scenarios.</p>

    <h4><strong>Equipment that has been mistreated or poorly maintained</strong></h4>
    <p class="article-content">If the appliance has been severely damaged due to mistreatment or lack of maintenance, you may run into trouble filing a claim to get it fixed. Stay on top of regular maintenance like A/C tuneups/filter changes and use the appliances for their intended purpose (only food scraps in the garbage disposal, no experiments with the microwave, etc.), and you should be good to go.</p></span>

    <p class="article-content">Though coverage will vary among different warranty services, this would serve as a solid list of what not to expect. Take the time to empower yourself by looking over a few warranty services and have questions ready for the salesmen so that you are not blindsided down the road when something breaks. That brings me to my next point..</p>

    <p align="center"><img class="article-img" src="http://www.speedyappliance.com/img/appliance-repair.png" alt="repairman"></p><br>
    <h2>Know what to do when an appliance breaks.</h2><br>
        <p class="article-content">First, breathe a sigh of relief that you had the foresight to purchase a warranty service :) Then, moving on to the actual repair process, you'll want to contact your warranty company before reaching out to a repair service. This is especially important, as the company may not process your claim if they are not consulted before a serviceperson diagnoses the issue.</p>

    <p class="article-content">Once you reach out to submit your claim, they'll work with you to schedule one of their vetted technicians to come out, troubleshoot the issue, and make any necessary repairs.</p>

    <p align="center"><img class="article-img" alt="contract" src="http://suebonzellrealestate.com/wp-content/uploads/2015/01/Contract2.jpg"></p><br>

    <h2>Know your contract.</h2><br>

    <p class="article-content">You may get some pushback or an outright 'no' from a representative when you submit a claim. Any denial of service should come with a clear and reasonable stance on the 'why?' behind the decision. If you don't feel the representative is providing that or they just aren't hearing you out, ask to speak to a manager.</p>
    <p class="article-content">Just remember that making your point calmly, but persistently, is always more effective than blind rage. Knowing the ins and outs of your contract will give you firm ground to stand on if you should have to argue your point.</p><br>

